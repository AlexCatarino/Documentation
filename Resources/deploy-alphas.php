<p>Follow these steps to deploy an Alpha to live trading:</p>
<ol>
    <li>Log in to the Algorithm Lab.</li>
    <li>In the left navigation bar, click <span class='menu-name'>Strategies &gt; Subscriptions &amp; Bids</span>.</li>
    <li>On the Subscriptions &amp; Bids page, click <span class='button-name'>Deploy</span> next to the Alpha that you want to deploy.</li>
    <li>Follow the <a href="<?= DOCS_ROOT_URL ?>/our-platform/tutorials/live-trading/brokerages">live trading tutorial</a> of the brokerage that you want to use when deploying the Alpha.</li>
    <p>If the capital in your brokerage account that you use to deploy the Alpha exceeds the allocation size granted by your Alpha subscription, the deployment will fail.</p>
</ol>