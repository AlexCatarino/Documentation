<p>Follow these steps to upload files to the Object Store:</p>

<ol>
	<li>Open the <a href='/docs/v2/cloud-platform/object-store#02-View-Storage'>Object Store</a> page.</li>
	<li>Navigate to the directory in the Object Store where you want to upload files.</li>
	<li>Click <span class='button-name'>Upload</span>.</li>
	<li>Drag and drop the files you want to upload.</li>
</ol>

<p>Alternatively, you can <a href='/docs/v2/writing-algorithms/object-store#04-Save-Data'>add data to the Object Store in an algorithm</a> or <a href='/docs/v2/research-environment/object-store#04-Save-Data'>notebook</a>.</p>
