<p>Follow these steps to delete storage from the Object Store:</p>

<ol>
	<li>Open the <a href='<?=$objectStoreLink?>'>Object Store</a> page.</li>
	<li>Navigate to the directory in the Object Store where you want to delete files.</li>
	<li>Click the check box next to the files you want to delete.</li>
	<li>Click <span class='button-name'>Actions</span> and then click <span class="button-name">Delete</span> from the drop-down menu.</li>
	<li>Click <span class='button-name'>OK</span>.</li>
</ol>

<p>Alternatively, you can <a href='/docs/v2/writing-algorithms/object-store#06-Delete-Data'>delete storage in an algorithm</a> or <a href='/docs/v2/research-environment/object-store#06-Delete-Data'>notebook</a>.</p>
