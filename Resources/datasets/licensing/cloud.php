<p>If you have a Cloud license for a dataset, you can access the dataset <?=$location?> for research, backtests, and live trading. The cost of the license is added to your monthly bill, which you can pay with your organization's credit card or <a href='/docs/v2/cloud-platform/organizations/credit'>QuantConnect Credit</a> balance. With one Cloud license for a dataset, all of the members in your organization can access the dataset in the cloud.</p>

<h4>Add Cloud Access</h4>
<p>You need an organization above the <a href='/docs/v2/cloud-platform/organizations/tier-features#02-Free-Tier'>Free tier</a> to purchase cloud access to datasets.</p>

<p>Follow these steps to add cloud access to datasets:</p>

<ol>
    <li>Log in to the Algorithm Lab.</li>
    <li>Open the <a href='/docs/v2/cloud-platform/datasets/navigating-the-market#02-View-All-Listings'>listing page of a dataset</a> for which you want to gain cloud access.</li>
    <li>On the dataset listing page, click the <span class='tab-name'>Pricing</span> tab.</li>
    <li>Under the <span class='page-section-name'>Cloud Access</span> section, click <span class='button-name'>SUBSCRIBE</span>.</li>
    <li>On the Pricing page, click <span class='button-name'>Proceed to Checkout</span>.</li>
</ol>


<h4>Remove Cloud Access</h4>
<p>Follow these steps to remove cloud access to datasets:</p>

<ol>
    <li>Open the <a href="/docs/v2/cloud-platform/organizations/administration#05-View-the-Organization-Homepage">organization homepage</a>.</li>
    <li>Click <span class='button-name'>Edit Plan</span>.</li>
    <li>On the pricing page, click the <span class='tab-name'>Customize Plan > Build Your Own Pack > Data</span> tab.</li>
    <li>In the <span class='page-section-name'>Datasets Subscriptions</span> section, next to the name of the dataset you want to remove, click <span class='button-name'>Added</span>.</li>
    <li>Click <span class='button-name'>Proceed to Checkout</span>.</li>
</ol>
