<p>Follow these steps to rename projects:</p>

<ol>
    <li><a href="/docs/v2/our-platform/tutorials/projects/managing-projects#05-Open-Existing-Projects">Open the project</a> that you want to rename.</li>
    <li>In the project panel, hover over the project name and then click the <span class='icon-name'>pencil</span> icon that appears.</li>
    <img class='docs-image' style='max-height: 120px' src="https://cdn.quantconnect.com/i/tu/rename-project.jpg">
    <li>Enter the new project name and then click <span class='button-name'>OK</span>.</li>
    <p>"Project renamed successfully" displays.</p>
</ol>