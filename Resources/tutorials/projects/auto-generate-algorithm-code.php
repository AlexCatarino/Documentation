<p>Follow these steps to add and remove modules to your algorithm:</p>

<ol>
    <li>Open a new project.</li>
    <li>At the top of the IDE, click the <span class='tab-name'>Strategy Builder</span> tab.</li>
    <p>The canvas displays.</p>
    <img class='docs-image' src='https://cdn.quantconnect.com/i/tu/strategy-builder-canvas.png'>
    <li>On the Strategy Builder page, click <span class="button-name">Add Module</span>.</li>
    <p>The modules catalog displays.</p>
    <img class="docs-image" src="https://cdn.quantconnect.com/i/tu/strategy-builder-catalog.png">
    <li><span class="qualifier">(Optional)</span> At the bottom of the page, click the pagination buttons to view other modules.</li>
    <li><span class="qualifier">(Optional)</span> Click a module in the table to view more information about it.</li>
    <li>To apply filters to the module catalog, follow these steps:</li>
    <ol>
        <li>Click <span class="button-name">Filters</span>.</li>
        <li>Select the column to which you want to apply the filter.</li>

            <p>The following table describes the columns that you can use:</p>

            <table class="table qc-table">
                <thead>
                    <tr>
                        <th>Column</th>
                        <th>Description</th>
                    </tr>
                </thead>
                <tbody>
                    <tr>
                        <td><span class="column-name">Name</span></td>
                        <td>To specify which characters need to be included in the module name to be included in the filtered results</td>
                    </tr>
                    <tr>
                        <td><span class="column-name">Category</span></td>
                        <td>To specify which type of modules should be included in the filtered results</td>
                    </tr>
                    <tr>
                        <td><span class="column-name">Author</span></td>
                        <td>To specify which characters need to be included in the author name to be included in the filtered results</td>
                    </tr>
                </tbody>
            </table>

        <li>If you selected the <span class="button-name">Name</span> column, enter the characters you want to be included in the module name.</li>
        <p>The module catalog displays modules that contain the characters you entered.</p>
        <li>If you selected the <span class="button-name">Category</span> column, select the check boxes next to the categories you want to be included in the results.</li>
        <p>The module catalog displays modules that are in the categories you selected.</p>
        <li>If you selected the <span class="button-name">Author</span> column in step 5.2, enter the characters you want to be included in the author name.</li>
        <p>The module catalog displays modules that have author names containing the characters you entered.</p>
    </ol>
    <img class="docs-image" src="https://cdn.quantconnect.com/i/tu/add-strategy-builder-module.gif">
    <li>Click <span class="button-name">Add</span> next to the modules you want to add.</li>
    <p>The modules you add are added to your code files.</p>
    <li>If you want to remove modules that you have added, click <span class="button-name">Remove</span> next to the module.</li>
    <p>The modules you remove are removed from your code files.</p>
</ol>
