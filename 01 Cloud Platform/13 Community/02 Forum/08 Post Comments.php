<p>You can only post comments on open forum discussions. To open a closed discussion, <a href='/contact'>contact us</a>.</p>

<p>Follow these steps to post a comment on a forum discussion:</p>

<ol>
    <li>Log in to your account.</li>
    <li>Open the <a href="/forum">forum homepage</a>.</li>
    <li>Click the discussion on which you want to comment.</li>
    <li>If you want to reply to a comment in the discussion, click <span class='button-name'>Reply</span> at the bottom of the comment.</li>
    <li>If you want to comment on the top-level discussion, scroll to the bottom of the discussion page and enter your comment.</li>
    <li><span class='qualifier'>(Optional)</span> If you comment on the top-level discussion, follow these steps to attach a backtest:</li>
    <ol>
        <li>Click <span class='button-name'>Attach Backtest</span>.</li>
        <li>Click the <span class='field-name'>Project</span> field and then select the project that contains the backtest you want to attach from the drop-down menu.</li>
        <p>You can only attach projects that you own.</p>
        <li>Click the <span class='field-name'>Backtest</span> field and then select a backtest from the drop-down menu.</li>
    </ol>
    <p>When you attach a backtest, you're sharing all of the project files that were used to generate the backtest results.</p>
    <li>Click <span class='button-name'>Reply</span>.</li>
    <p>Your comment is appended to the discussion.</p>
</ol>
