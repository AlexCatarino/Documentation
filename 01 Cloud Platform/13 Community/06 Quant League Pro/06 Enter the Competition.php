<p>To add an algorithm to the competition, fill out the <a href='/league/join'>Pro League Submission</a> form.</p>

<p>You must observe the following rules:</p>
<ul>
  <li><b>You must be proficient at <a href='/docs/v2/writing-algorithms'>coding with the QuantConnect API</a>.</b></li>
  <li>Cash deposits during the quarter result in disqualification.</li>
  <li>Copy QuantConnect into correspondence with investment funds.</li>
  <li>Keep a QuantConnect public strategy listing if you receive an allocation.</li>
  <li>Deploy all allocations through the QuantConnect platform.</li>
</ul>

<p>
    Our goal isn't to charge users, but to grow institutional clients on QuantConnect and provide opportunities for users. 
    To do this, we ask that allocations are done on a QuantConnect live server and that QuantConnect is copied into correspondence so we understand the challenges and process for the users. 
    If you receive an allocation, keep the alpha listed. 
    Otherwise, all the good ones would be immediately picked off and there would be no incentive for funds to browse the listing and no competing bids to license your alpha.
</p>

<p>Your algorithm must observe the following rules:</p>
<ul>
  <li>Your algorithm is unique.</li>
  <li>Your algorithm is actively trading.</li>
  <li>Your algorithm trades liquid <a href="/docs/v2/writing-algorithms/securities/key-concepts">securities</a>. For Equities, this includes the top 1,000 stocks, Crypto includes the top 10 coins, Forex includes the top 20 pairs, and Futures includes the top 30 contracts.</li>
  <li>The maximum leverage is 2.</li>
</ul>
